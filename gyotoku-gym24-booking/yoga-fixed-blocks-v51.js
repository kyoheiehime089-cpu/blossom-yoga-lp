// v51: 固定プログラム枠の表示名を補正します。
// 既存の yoga-private-clean-v39.js が持つ fixedBlocks を後読みで差し替えます。
function fixedBlocks(date){
  const day=new Date(date+'T00:00:00').getDay();
  // 木曜日はセミパーソナル休み。12:00通常ヨガ、20:45通常ヨガのみ表示します。
  if(day===4)return[[690,790,'通常ヨガ'],[1215,1315,'通常ヨガ']];
  if(HOLIDAYS.includes(date))return[[510,820,'通常ヨガ/セミパーソナル枠']];
  if(day===1)return[[510,610,'通常ヨガ'],[1080,1330,'セミパーソナル']];
  // 火曜日12:00〜13:40はセミパーソナルではなく通常ヨガです。
  if(day===2)return[[510,610,'通常ヨガ'],[720,820,'通常ヨガ'],[1080,1330,'セミパーソナル']];
  if(day===3)return[[1080,1330,'セミパーソナル']];
  if(day===5)return[[1080,1330,'セミパーソナル']];
  if(day===6||day===0)return[[460,820,'通常ヨガ/セミパーソナル枠']];
  return[];
}